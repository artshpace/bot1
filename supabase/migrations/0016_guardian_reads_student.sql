-- =====================================================================
-- Phase 3 — фикс: опекун должен читать запись ребёнка в ростере
-- ---------------------------------------------------------------------
-- Баг: students_select (0011) разрешал чтение students только персоналу и
-- самому ученику (user_id). Родитель не мог прочитать запись своего ребёнка,
-- из-за чего «Карта развития ребёнка» в кабинете родителя не находила
-- ученика и показывала «нет учеников».
--
-- Добавляем опекуна (is_guardian из 0013) в политику чтения students.
--
-- Run в Supabase → SQL Editor → Run. ПОСЛЕ 0013/0015. Идемпотентно.
-- =====================================================================

drop policy if exists "students_select" on public.students;
create policy "students_select" on public.students for select
  using ( public.is_staff() or user_id = auth.uid() or public.is_guardian(id) );
